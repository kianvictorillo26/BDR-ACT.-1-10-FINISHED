    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
        String usernameInput = user.getText().trim();
        String oldPassword = new String(oldpassword.getPassword()).trim();
        String newPassword = new String(newpass.getPassword()).trim();
        String confirmPassword = new String(confirmpass.getPassword()).trim();

        if (usernameInput.isEmpty() || oldPassword.isEmpty() || newPassword.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!newPassword.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "New password and confirm password do not match!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            config.config db = new config.config();
            try (Connection con = db.getConnection()) {
                // Verify old password
                String selectSql = "SELECT pname FROM users WHERE uname = ?";
                try (PreparedStatement selectPst = con.prepareStatement(selectSql)) {
                    selectPst.setString(1, usernameInput);
                    try (ResultSet rs = selectPst.executeQuery()) {
                        if (rs.next()) {
                            String storedHashedPassword = rs.getString("pname");
                            if (!passwordHasher.checkPassword(oldPassword, storedHashedPassword)) {
                                JOptionPane.showMessageDialog(this, "Incorrect old password!", "Error", JOptionPane.ERROR_MESSAGE);
                                return;
                            }
                            if (passwordHasher.checkPassword(newPassword, storedHashedPassword)) {
                                JOptionPane.showMessageDialog(this, "New password cannot be the same as the old password!", "Error", JOptionPane.ERROR_MESSAGE);
                                return;
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "User not found!", "Error", JOptionPane.ERROR_MESSAGE);
                            return;
                        }
                    }
                }

                // Hash the new password
                String hashedNewPassword = passwordHasher.hashPassword(newPassword);

                String updateSql = "UPDATE users SET pname = ? WHERE uname = ?";
                try (PreparedStatement updatePst = con.prepareStatement(updateSql)) {
                    updatePst.setString(1, hashedNewPassword);
                    updatePst.setString(2, usernameInput);

                    int updated = updatePst.executeUpdate();
                    if (updated > 0) {
                        JOptionPane.showMessageDialog(this, "Password updated successfully!");

                        this.dispose(); // Close reset password form

                        // Redirect to login
                        new loginForm().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Password update failed!", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_saveMouseClicked

