    private void checkusernameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkusernameActionPerformed
        String username = user.getText().trim();
        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a username.");
            return;
        }
        
        config conf = new config();
        try (Connection conn = conf.getConnection()) {
            String sql = "SELECT security_question FROM users WHERE uname = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, username);
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                String securityQuestion = rs.getString("security_question");
                if (securityQuestion != null) {
                    sec.setText(securityQuestion);
                } else {
                    sec.setText("");
                    JOptionPane.showMessageDialog(this, "No security question set for this user.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Username not found.");
                sec.setText("");
            }
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error fetching security question: " + e.getMessage());
        }
    }//GEN-LAST:event_checkusernameActionPerformed

    private void submitforgotpasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitforgotpasswordMouseClicked
        String username = user.getText().trim();
        String securityAnswer = sa.getText().trim();

        if (username.isEmpty() || securityAnswer.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        config conf = new config();
        try (Connection conn = conf.getConnection()) {
            String sql = "SELECT security_answer FROM users WHERE uname = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, username);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String correctAnswer = rs.getString("security_answer");
                if (correctAnswer != null && correctAnswer.equalsIgnoreCase(securityAnswer)) {
                    // Proceed to reset password screen
                    resetPassword resetForm = new resetPassword();
                    resetForm.setUsername(username);
                    resetForm.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Incorrect security answer.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Username not found.");
            }
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error verifying security answer: " + e.getMessage());
        }
    }//GEN-LAST:event_submitforgotpasswordMouseClicked

